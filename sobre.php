        <h4>Sobre</h4>
        <p>O <span>Mulher Tech Sim Senhor</span> é um evento anual idealizado pelo Women Techmakers João Pessoa com o objetivo de reunir mulheres de TI em discussões sobre igualdade e diversidade, e também em oficinas sobre novas tecnologias. Desde 2016, o evento vem sendo realizado numa parceria entre diversos grupos que têm atividades voltadas ao tema Gênero em TI. 
