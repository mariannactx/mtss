    <div class="content-sobre">
        <p>É um evento anual idealizado pelo Women Techmakers João Pessoa com o objetivo de reunir mulheres de TI em discussões sobre igualdade e diversidade, como também em oficinas sobre novas tecnologias. Desde 2016, o evento vem sendo realizado numa parceria entre diversos grupos que têm atividades voltadas ao tema de diversidade de gênero em TI. 
        </p>

        <p>Em 2019, a proposta é integrar mulheres estudantes ou atuantes no mercado de tecnologia da informação.</p>
        
